namespace Jeebs.Services.Drawing
{
	/// <summary>
	/// Supported image formats
	/// </summary>
	public enum ImageFormat
	{
		/// <summary>
		/// Bitmap format
		/// </summary>
		Bmp,

		/// <summary>
		/// Graphics Interchange Format (format)
		/// </summary>
		Gif,

		/// <summary>
		/// Icon
		/// </summary>
		Ico,

		/// <summary>
		/// Joint Photographic Experts Group format
		/// </summary>
		Jpeg,

		/// <summary>
		/// Portable Network Graphics format
		/// </summary>
		Png
	}
}
